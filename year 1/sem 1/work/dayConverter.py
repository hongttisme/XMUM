years = int(input("Please enter number of years: "))
days = years * 365
hours = days * 24
minutes = hours * 60
seconds = minutes * 60
print(f"1 years equivalent to {days} days")
print(f"1 years equivalent to {hours} hours")
print(f"1 years equivalent to {minutes} minutes")
print(f"1 years equivalent to {seconds} seconds")
